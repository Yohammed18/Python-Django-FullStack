# Learning numbers
a = 12
b = 20

print(a*b)
print(a-b)
print(a+b)
print(a/b)

# Subtraction
result_subtraction = 10 - 4
print("Subtraction:", result_subtraction)  # Output: 6

# Multiplication
result_multiplication = 6 * 7
print("Multiplication:", result_multiplication)  # Output: 42

# Division
result_division = 15 / 3
print("Division:", result_division)  # Output: 5.0 (in Python 3, division always returns a float)

# Integer Division (floor division)
result_floor_division = 17 // 5
print("Floor Division:", result_floor_division)  # Output: 3

# Modulo (remainder)
result_modulo = 17 % 5
print("Modulo:", result_modulo)  # Output: 2

# Exponentiation
result_exponentiation = 2 ** 3
print("Exponentiation:", result_exponentiation)  # Output: 8